public class Display {

    public static void main(String[] args) {
        int[] array = {10, 20, 30, 40, 50};

        System.out.println("Array contents:");
        for (int i = 0; i < array.length; i++) {
            System.out.print(array[i] + " ");
        }
        System.out.println();
    }
}
